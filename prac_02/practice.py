# Maths Operators
"""
get lhs, rhs
print lhs + rhs
print lhs - rhs
print lhs * rhs
print lhs / rhs
print lhs % rhs
print lhs ** rhs
print lhs // rhs
"""
lhs = float(input("Please input the 1st number: "))
rhs = float(input("Please input the 2nd number: "))
print(lhs, '+', rhs, 'is', lhs + rhs)
print(lhs, '-', rhs, 'is', lhs - rhs)
print(lhs, '*', rhs, 'is', lhs * rhs)
print(lhs, '/', rhs, 'is', lhs / rhs)
print(lhs, '%', rhs, 'is', lhs % rhs)
print(lhs, '**', rhs, 'is', lhs ** rhs)
print(lhs, '//', rhs, 'is', lhs // rhs)

